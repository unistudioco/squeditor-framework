const fs = require('fs');
const path = require('path');

// Resolve project root (cwd when running npm scripts)
const projectRoot = process.cwd();
const config = require(path.join(projectRoot, 'squeditor.config.js'));
const fwRoot = path.resolve(projectRoot, config.framework); // resolves ..
const manifest = require(path.join(fwRoot, 'uikit-manifest.json'));
const resolvePages = require('./utils/resolve-pages');


const selectedComponents = config.components || [];

// JS output still relies on concatenation
const outputJsDir = path.join(projectRoot, config.output.js);
fs.mkdirSync(outputJsDir, { recursive: true });
// CSS output changes to dynamic SCSS generation
const outputScssDir = path.join(projectRoot, 'src/assets/scss');
fs.mkdirSync(outputScssDir, { recursive: true });
// JS and SCSS collectors
const jsPaths = new Set();
const scssPaths = new Set();

// 1. Core JS and SCSS
const core = manifest['_core'];
core.js.forEach(f => jsPaths.add(f));
core.css.forEach(f => scssPaths.add(f));

// 2. Component JS and SCSS
selectedComponents.forEach(componentName => {
  const entry = manifest[componentName];
  if (!entry) {
    console.warn(`[Squeditor] Unknown component in config: "${componentName}" — skipping.`);
    return;
  }
  entry.js.forEach(f => jsPaths.add(f));
  entry.css.forEach(f => scssPaths.add(f));
});

// 3. Generate Bundles
let jsBundle = '';
jsPaths.forEach(f => {
  const filePath = path.join(projectRoot, f.replace('node_modules/uikit/', 'node_modules/uikit/'));
  // Special case for icon which points to dist/ specifically in manifest
  const iconPath = path.join(projectRoot, f);
  const finalPath = fs.existsSync(iconPath) ? iconPath : path.join(projectRoot, 'node_modules/uikit', f.replace('node_modules/uikit/', ''));

  if (fs.existsSync(finalPath)) {
    jsBundle += fs.readFileSync(finalPath, 'utf8') + '\n';
  } else {
    console.warn(`[Squeditor] file not found: ${finalPath}`);
  }
});

let scssImports = '// Auto-generated by build-components.js — DO NOT EDIT\n';
scssImports += '// These imports point to the UIKit SCSS source files to enable tree-shaking natively via Vite\n\n';
scssImports += '// Core UIkit Dependencies\n';

scssPaths.forEach(f => {
  const absolutePath = path.join(projectRoot, f);
  const relativePath = path.relative(outputScssDir, absolutePath).replace(/\\/g, '/');
  if (f.includes('variables.scss') || f.includes('mixins.scss')) {
    scssImports += `@import "${relativePath}";\n`;
  }
});

scssImports += '\n// Active UIkit Components\n';
scssPaths.forEach(f => {
  if (!f.includes('variables.scss') && !f.includes('mixins.scss')) {
    const absolutePath = path.join(projectRoot, f);
    const relativePath = path.relative(outputScssDir, absolutePath).replace(/\\/g, '/');
    scssImports += `@import "${relativePath}";\n`;
  }
});

// Finalize writes
fs.writeFileSync(path.join(outputJsDir, 'uikit-components.js'), jsBundle);
fs.writeFileSync(path.join(outputScssDir, '_uikit_dynamic.scss'), scssImports);

console.log('[Squeditor] ✅ UIkit3 components built successfully.');

// Generate src/config/active-components.php for the style-guide page
const phpConfigDir = path.join(projectRoot, 'src/config');
fs.mkdirSync(phpConfigDir, { recursive: true });
const phpComponentsArray = `[${selectedComponents.map(c => `'${c}'`).join(', ')}]`;
const phpConfig = `<?php\n// Auto-generated by build-components.js — DO NOT EDIT\n$active_components = ${phpComponentsArray};\n`;
fs.writeFileSync(
  path.join(phpConfigDir, 'active-components.php'),
  phpConfig
);

// Generate src/config/active-themes.php for automatic PHP dev server theme detection
// Map each page to its corresponding theme identifier
const themePageMapping = {};
if (config.themes) {
  Object.keys(config.themes).forEach(themeKey => {
    const rawPages = config.themes[themeKey].pages || [];
    const absolutePages = resolvePages(rawPages, projectRoot);
    absolutePages.forEach(page => {
      // Clean leading slashes for normalization if desired, but here we just map exact values
      themePageMapping[page] = themeKey;
    });
  });
}
const phpThemesArrayParts = Object.entries(themePageMapping).map(([page, theme]) => `'${page}' => '${theme}'`);
const phpThemesConfig = `<?php\n// Auto-generated by build-components.js — DO NOT EDIT\n$active_themes = [${phpThemesArrayParts.join(', ')}];\n`;
fs.writeFileSync(
  path.join(phpConfigDir, 'active-themes.php'),
  phpThemesConfig
);

// Generate src/config/theme-entries.php — lists all theme keys for CSS loading in head.php
if (config.themes) {
  const themeKeys = Object.keys(config.themes).map(k => `'${k}'`).join(', ');
  const phpThemeEntries = `<?php\n// Auto-generated by build-components.js — DO NOT EDIT\n// All theme CSS entry keys (used by head.php to load theme-*.css files)\n$theme_entries = [${themeKeys}];\n`;
  fs.writeFileSync(
    path.join(phpConfigDir, 'theme-entries.php'),
    phpThemeEntries
  );

  // Generate src/config/site-settings.php — general flags (demo mode vs customer mode)
  const phpSettings = `<?php\n// Auto-generated by build-components.js — DO NOT EDIT\n$site_settings = [\n    'is_demo_mode' => true,\n];\n`;
  fs.writeFileSync(
    path.join(phpConfigDir, 'site-settings.php'),
    phpSettings
  );
}


// Generate standalone theme entry files instead of injecting into main.scss
// This allows Vite to compile each theme as a separate CSS file
const mainScssPath = path.join(projectRoot, 'src/assets/scss/main.scss');
if (fs.existsSync(mainScssPath) && config.themes) {
  let mainScss = fs.readFileSync(mainScssPath, 'utf8');

  // Strip out any existing theme imports from main.scss (cleanup from old approach)
  mainScss = mainScss.replace(/@import\s+['"]themes\/[^'"]+['"];\n?/g, '');
  mainScss = mainScss.trim() + '\n';
  fs.writeFileSync(mainScssPath, mainScss);

  // Generate a standalone SCSS entry file for each theme
  // These are non-partials (no underscore prefix) so Vite treats them as entry points
  const themesDir = path.join(projectRoot, 'src/assets/scss');
  Object.keys(config.themes).forEach(themeKey => {
    const entryContent = `// Auto-generated by build-components.js — DO NOT EDIT
// Standalone theme entry: compiled to theme-${themeKey}.css by Vite
@import 'config';
@import 'functions';
@import 'theme-engine';
@import 'themes/${themeKey}';
`;
    fs.writeFileSync(path.join(themesDir, `theme-${themeKey}.scss`), entryContent);
  });

  console.log(`[Squeditor] 🎨 Generated theme entries: ${Object.keys(config.themes).map(k => 'theme-' + k + '.scss').join(', ')}`);
}

// Generate Dynamic Slider Config Import
const sliderConfig = config.slider || { library: false };
const dynamicSliderPath = path.join(outputJsDir, '_slider_dynamic.js');
let sliderImportCode = '// Auto-generated by build-components.js — DO NOT EDIT\n';
if (sliderConfig.library === 'swiper') {
    sliderImportCode += 'import \'./modules/swiper-init.js\';\n';
} else if (sliderConfig.library === 'splide') {
    sliderImportCode += 'import \'./modules/splide-init.js\';\n';
}
fs.writeFileSync(dynamicSliderPath, sliderImportCode);
console.log(`[Squeditor] 🎠 Selected slider library: ${sliderConfig.library || 'none'}`);

// Copy slider library CSS to src/assets/css/slider.min.css
// This keeps the CSS separate from main.js and gives it a clear, descriptive filename
const sliderCssDest = path.join(projectRoot, 'src/assets/css/slider.min.css');
if (sliderConfig.library === 'splide') {
    const splideCssPath = path.join(projectRoot, 'node_modules/@splidejs/splide/dist/css/splide.min.css');
    if (fs.existsSync(splideCssPath)) {
        fs.copyFileSync(splideCssPath, sliderCssDest);
    } else {
        console.warn('[Squeditor] ⚠️ Splide CSS not found at expected path.');
        fs.writeFileSync(sliderCssDest, '/* Splide CSS not found */\n');
    }
} else if (sliderConfig.library === 'swiper') {
    // Concatenate all Swiper CSS modules into a single file
    const swiperCssParts = [
        'node_modules/swiper/swiper.min.css',
        'node_modules/swiper/modules/navigation.min.css',
        'node_modules/swiper/modules/pagination.min.css',
        'node_modules/swiper/modules/effect-fade.min.css',
        'node_modules/swiper/modules/free-mode.min.css',
    ];
    let combinedCss = '/* Swiper CSS - auto-generated by build-components.js */\n';
    for (const part of swiperCssParts) {
        const fullPath = path.join(projectRoot, part);
        if (fs.existsSync(fullPath)) {
            combinedCss += fs.readFileSync(fullPath, 'utf8') + '\n';
        }
    }
    fs.writeFileSync(sliderCssDest, combinedCss);
} else {
    // No slider configured — write an empty placeholder so head.php link doesn't 404
    fs.writeFileSync(sliderCssDest, '/* No slider library configured */\n');
}

// Generate src/config/active-slider.php for conditional CSS loading in head.php
const phpSliderConfig = `<?php\n// Auto-generated by build-components.js — DO NOT EDIT\n$active_slider = ${sliderConfig.library ? `'${sliderConfig.library}'` : 'false'};\n`;
fs.writeFileSync(
  path.join(phpConfigDir, 'active-slider.php'),
  phpSliderConfig
);
